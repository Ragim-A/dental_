$(document).ready(function() {
    $('.testimonials .dot').click(function() {
        $('.testimonials .dot').removeClass('dotActive');
        $('.testimonials .dotty').removeClass('dottyActive');
        $(this).addClass('dotActive');
        $(this).parent().addClass('dottyActive');
    });
});
